     *
     * @param  \App\Models\Banner  $banner
     * @return \Illuminate\Http\Response
     */
    public function edit(Banner $banner)
    {
        $this->authorize("banner/edit", $banner);
        return view("hero.edit", compact("banner"));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\Banner  $banner
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Banner $banner)
    {
        $request->validate([
            "titre" => "required",
            "boutton" => "required",
            "path" => "required",
        ]);
        $banner->titre = $request->titre;
        $banner->boutton = $request->boutton;
        Storage::disk("public")->delete("img/" . $banner->path);
        $banner->path = $request->file("path")->hashName();
        $request->file("path")->storePublicly("img", "public");
        $banner->save();
        return redirect()->route("menu");
    }

